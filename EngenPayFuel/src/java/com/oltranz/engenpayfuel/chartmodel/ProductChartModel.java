package com.oltranz.engenpayfuel.chartmodel;

/**
 *
 * @author John
 */
public class ProductChartModel {
    
    private String name;
    private double superr;
    private double gasoil;
    
    /**
     * @return the superr
     */
    public double getSuperr() {
        return superr;
    }

    /**
     * @param superr the superr to set
     */
    public void setSuperr(double superr) {
        this.superr = superr;
    }

    /**
     * @return the gasoil
     */
    public double getGasoil() {
        return gasoil;
    }

    /**
     * @param gasoil the gasoil to set
     */
    public void setGasoil(double gasoil) {
        this.gasoil = gasoil;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    
    
}
